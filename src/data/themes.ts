export function getTheme(themeId: string): VisualTheme | undefined {
  return THEMES.find((t) => t.id === themeId);
}

export function getThemesByCategory(category: string): VisualTheme[] {
  if (category === "All") return THEMES;
  return THEMES.filter((t) => t.category === category);
}

export function getCategories(): string[] {
  return ["All", ...Array.from(new Set(THEMES.map((t) => t.category)))];
}
